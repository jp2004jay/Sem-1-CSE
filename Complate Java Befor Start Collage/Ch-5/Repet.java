package ch_5;
public class Repet
{
		public static void main (String [] args)
		{
			//Repetation for using while loop.
			int i=0;
			while(i<8)
			{
				System.out.println("5");
				i++;	
			}
			
			
			System.out.println("\n");
			//Repetation fot using for loop.
			for(int j=0; j<5; j++)
			{
				System.out.print("1");
			}
			
			System.out.println("\n");
			//Repetation fot using for loop.
			for(int k=0; k<8; k++)
			{
				System.out.println("2");
			}
		}
}